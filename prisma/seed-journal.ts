type Section = {
  h2: string
  content: string
  subsections?: { h3: string; content: string }[]
}

function generateHTML(sections: Section[]): string {
  return sections.map((section, i) => {
    let html = `<h2 id="section-${i + 1}">${section.h2}</h2>\n`
    html += `<p>${section.content}</p>`
    if (section.subsections) {
      section.subsections.forEach((sub, j) => {
        html += `\n<h3 id="section-${i + 1}-${j + 1}">${sub.h3}</h3>\n`
        html += `<p>${sub.content}</p>`
      })
    }
    return html
  }).join("\n\n")
}

function randomDate(start: Date, end: Date): Date {
  return new Date(start.getTime() + Math.random() * (end.getTime() - start.getTime()))
}

