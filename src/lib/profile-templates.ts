/**
 * 業種カテゴリと組み合わせて、対象フィールドの利用可能テンプレートを返す。
 * カテゴリ一致 + 共通（category: null）を結合。
 */
export function templatesForField(
  field: TemplateField,
  category: string | null
): ProfileTemplate[] {
  const all = PROFILE_TEMPLATES[field] ?? []
  return all.filter((t) => t.category === null || t.category === category)
}
